                <h1>O marco cívil</h1>

             
                
                <p>
                O Marco Civil da Internet, lei que funciona como uma Constituição para o uso da rede no Brasil, 
                entra em vigor nesta segunda-feira (23). O projeto foi sancionado pela presidente Dilma Rousseff em 23 de abril 
                após tramitar por dois anos na Câmara dos Deputados e estabelece princípios, garantias, direitos e deveres para 
                internautas e empresas.
                </p>

                <p>
                A chamada neutralidade de rede é um dos principais pilares do Marco Civil. Com ela, os provedores de internet ficam 
                proibidos de ofertar conexões diferenciadas a partir do conteúdo que o usuário for acessar, como e-mails,
                vídeos ou redes sociais. A venda de velocidades diferentes de acesso continua valendo.
                </p>

                <p>
                 Críticos da neutralidade dizem que o princípio restringe a liberdade dos 
                 provedores para oferecer conexões diferenciadas conforme a demanda dos
                 clientes e que sua aplicação obrigatória pode encarecer o serviço a todos.   
                </p>

                <img src="img/img1.jpg">


                <h1>Sobre a privacidade</h1>

                <p>

                 Outra regulamentação do Marco Civil diz respeito à retirada de conteúdo da internet. Provedores de conexão e aplicações na 
                 internet não serão responsabilizados pelo uso que os internautas fizerem da rede e por publicações feitas por terceiros.   

                </p>

                <p>
                 Até então não havia regras específicas sobre o caso e as decisões judiciais variavam – alguns juízes 
                 punem sites como Facebook e Google 
                 por páginas ofensivas criadas por usuários, enquanto outros magistrados optam por penalizar 
                 apenas o responsável pelo conteúdo.
                </p>

                <p>
                 De acordo com a nova legislação, as entidades que oferecem conteúdo e aplicações só serão responsabilizadas 
                 por danos gerados por terceiros se não acatarem ordem judicial exigindo a retirada dessas publicações. 
                 O objetivo da norma, segundo o deputado Alessandro Molon, relator do projeto, 
                 é fortalecer a liberdade de expressão na web e acabar com o que chama de "censura privada".
                </p>

                <p>
                  O sigilo das comunicações dos usuários da internet não pode ser violado. 
                  Provedores de acesso à internet serão obrigados a guardar os registros das horas de acesso e do fim da conexão dos usuários
                  pelo prazo de seis meses, 
                  mas isso deve ser feito em ambiente controlado.  
                </p>

                <p>
                 Com o Marco Civil, as empresas de acesso não poderão "espiar" o conteúdo das informações 
                 trocadas pelos usuários na rede. Há interesse em fazer isso com fins comerciais e publicitários, 
                 nos moldes do que Facebook e Google 
                 fazem para enviar anúncios aos seus usuários de acordo com as mensagens que trocam.   

                </p>
                
